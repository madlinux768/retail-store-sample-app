package com.amazon.sample.orders.config.messaging;

import jakarta.validation.constraints.NotBlank;
import lombok.Getter;
import lombok.Setter;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.validation.annotation.Validated;

@ConfigurationProperties(prefix = AzureServiceBusProperties.PREFIX)
@Validated
@Getter
@Setter
public class AzureServiceBusProperties {

  public static final String PREFIX = "retail.orders.messaging.azureservicebus";

  @NotBlank(
    message = "retail.orders.messaging.azureservicebus.connectionString must not be blank"
  )
  private String connectionString;

  private String queueName;
}
